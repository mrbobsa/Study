package javastudy;

public class PrimitiveDataTypeExam {

	public static void main(String[] args) {
		boolean isFun = true;
		System.out.println(isFun);
		
//		값 하나만 입력가능
		char c = 'f';
		
		int x = 59;
		
//		얜 맨 뒤에 꼭 L을 붙여야 함
		long bing = 34545433L;
		
		float f = 32.4f;
		
		double d = 343434.5;
	}

}
